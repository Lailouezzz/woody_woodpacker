// Test 11: Math operations
#include <stdio.h>

int main(void) {
    int a = 42, b = 13;
    printf("Add: %d\n", a + b);
    printf("Sub: %d\n", a - b);
    printf("Mul: %d\n", a * b);
    printf("Div: %d\n", a / b);
    printf("Mod: %d\n", a % b);
    printf("And: %d\n", a & b);
    printf("Or: %d\n", a | b);
    printf("Xor: %d\n", a ^ b);
    return 0;
}
